    private void bCadastarVisitanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastarVisitanteActionPerformed
        // TODO add your handling code here:     
        // TODO add your handling code here:
        if(campoNomeVisitante.getText().isEmpty() || campoCPFVisitante.getText().isEmpty() || 
                campoTelefoneVisitante.getText().isEmpty() || campoPlacaVisitante.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }else{
        
        try{
       VisitanteController controller = new VisitanteController();
         // criando um objeto do tipo usuario da pasta model
            Visitante visita = new Visitante(
                campoNomeVisitante.getText(),
                campoCPFVisitante.getText(),
                campoTelefoneVisitante.getText(),
                campoPlacaVisitante.getText()
                
            );
            
                // chamando o metodo de cadastrar o usuario no banco de dados
            controller.cadastroVisita(visita);
            JOptionPane.showMessageDialog
            (null,"Visitante Cadastrado com Sucesso!");

          
            limparCampos();
            dispose();
        }catch(Exception e){
             JOptionPane.showMessageDialog
            (null,"Visitante NÃ£o Cadastrado! "+e);
        }
        }
    }//GEN-LAST:event_bCadastarVisitanteActionPerformed

